class User {
  final String id;
  final String username;
  final String email;
  final String city;
  final String state;
  final String country;
  final int skillId;
  final String level;
  final int points;

  User({
    required this.id,
    required this.username,
    required this.email,
    required this.city,
    required this.state,
    required this.country,
    required this.skillId,
    required this.level,
    required this.points,
  });
}
